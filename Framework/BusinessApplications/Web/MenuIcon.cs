namespace Framework.BusinessApplications.Web {

    using Presentation.Web;

    /// <summary>
    /// Control for menu icon to be used on web pages.
    /// </summary>
    internal sealed class MenuIcon : Control {

        /// <summary>
        /// Instiantiates a new instance.
        /// </summary>
        public MenuIcon()
            : base("span") {
            // nothing to do
        }

        /// <summary>
        /// Renders all child controls.
        /// </summary>
        /// <param name="html">HTML response</param>
        protected override void RenderChildControls(HtmlWriter html) {
            html.Append("&#xE5D4;");
            return;
        }

    }

}